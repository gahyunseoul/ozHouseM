package com.oz.ozHouse.dto;

public class ScrapDTO {
	private int scrap_num;
	private int member_num;
	private int product_num;
	private String scarp_date;
	public int getScrap_num() {
		return scrap_num;
	}
	public void setScrap_num(int scrap_num) {
		this.scrap_num = scrap_num;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public int getProduct_num() {
		return product_num;
	}
	public void setProduct_num(int product_num) {
		this.product_num = product_num;
	}
	public String getScarp_date() {
		return scarp_date;
	}
	public void setScarp_date(String scarp_date) {
		this.scarp_date = scarp_date;
	}
	
	
}
